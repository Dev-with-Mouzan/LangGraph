# What is Graph
"""
A graph is a data structure that consists of a set of nodes (also called vertices) and a set of edges that connect pairs of nodes. Graphs can be used to represent various types of relationships and connections between entities, such as social networks, transportation systems, and computer networks.


"""

# What is Node
"""
A node, also known as a vertex, is a fundamental unit of a graph. It represents an individual entity or point in the graph. Each node can have properties or attributes associated with it, such as a name, value, or other relevant information. Nodes are connected to each other through edges, which represent the relationships or connections between them.

"""


# What is Edge
"""
An edge is a connection between two nodes in a graph. It represents a relationship or interaction between the nodes it connects. Edges can be directed (pointing from one node to another) or undirected (indicating a mutual relationship). Edges can also have weights or labels associated with them, which can represent the strength of the connection or provide additional information about the relationship.

"""

# What is State
"""
In the context of graphs, a state refers to a specific configuration or condition of the graph at a given point in time. It can represent the current status of the nodes and edges, including their properties, values, and relationships. The state of a graph can change over time as nodes and edges are added, removed, or modified. Understanding the state of a graph is crucial for analyzing its structure and behavior, as well as for performing various operations such as traversal, search, and optimization.

"""